#include "libft.h"

void	ft_putnbr(int n)
{
	char	num;

	if (n < 0)
	{
		write (1, "-", 1);
		n *= -1;
	}
	if (n == -2147483648)
	{
		write (1, "2", 1);
		n = 147483648;
	}
	if (n >= 10)
		ft_putnbr(n / 10);
	num = n % 10 + '0';
	write (1, &num, 1);
}
